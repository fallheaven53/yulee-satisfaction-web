# 짧은 응답이면 풀 주소로 교체, 긴 주소면 원문 유지
SHORT_RAW_MAX = 6  # 'xxx구'·'xxx시'·'xxx군' 수준


def _ko_clean(s):
    if s is None:
        return ""
    s = str(s).strip().lstrip("?").strip()
    return s


def normalize_address(raw):
    """
    raw → (표준화 주소, 거리km|None)

    - raw가 짧고 키워드와 동일한 경우: 풀 주소로 교체 + 거리 부여
    - raw에 키워드가 포함된 긴 주소인 경우: 원문 유지 + 키워드 기반 거리 부여
    - 키워드를 찾지 못한 경우: 원문 유지 + 거리 None
    """
    s = _ko_clean(raw)
    if not s:
        return ("", None)

    # 키워드 탐색 — 긴 키워드부터 검사 ('광산구'가 '광'보다 먼저)
    sorted_keys = sorted(ADDRESS_MAP.keys(), key=lambda k: -len(k))
    matched = None
    for key in sorted_keys:
        if key in s:
            matched = key
            break

    if matched is None:
        return (s, None)

    full_addr, dist = ADDRESS_MAP[matched]

    # 짧은 응답 (키워드만 또는 약간의 장식)이면 풀 주소로 교체
    if len(s) <= SHORT_RAW_MAX:
        return (full_addr, dist)

    # 긴 주소는 원문 유지
    return (s, dist)


# 거리 구간 분류
DISTANCE_BUCKETS = [
    ("5km 미만",   0,   5),
    ("5~10km",     5,  10),
    ("10~20km",   10,  20),
    ("20km 이상", 20, 9999),
]


def classify_distance(km):
    if km is None:
        return None
    for label, lo, hi in DISTANCE_BUCKETS:
        if lo <= km < hi:
            return label
    return None
